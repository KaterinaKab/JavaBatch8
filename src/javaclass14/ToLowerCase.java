package javaclass14;

public class ToLowerCase {
    public static void main(String[] args) {
        String name="Ali Kursun";
        System.out.println(name.toLowerCase());
        name="ALI KURSIN";
        System.out.println(name.toLowerCase());
        System.out.println();

        String name2=name.toLowerCase();
        System.out.println(name);
        System.out.println(name2);

    }

}
